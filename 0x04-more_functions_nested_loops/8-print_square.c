#include "main.h"
/**
 * print_square - prints squares
 *
 * @size: size of the square.
 */
void print_square(int size)
{
	if (size <= 0)
	{
		_putchar('\n');
	}
	else
	{
		int i;
		int j;
		int n;

		for (i = 1; i <= n; n++)
		{
			for (j = 1; j <= n; j++)
			{
				_putchar('#');
			}
			_putchar('\n');
		}
	}
}
