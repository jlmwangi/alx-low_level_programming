#include <stdio.h>
/**
 * op_add - function to calculate sum
 * @a: integer
 * @b: integer
 *
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates subtraction
 * @a: integer
 * @b: integer
 *
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies integers
 * @a: integer
 * @b:integer
 *
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide integers
 * @a: integer
 * @b: integer
 *
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates modulus
 * @a: integer
 * @b: integer
 *
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
